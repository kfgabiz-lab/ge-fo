export const sectionGuideEntryCount = sectionGuideCategories.reduce(
  (sum, category) => sum + category.entries.length,
  0,
);

export function getSectionGuideEntry(id: string): SectionGuideEntry | undefined {
  for (const category of sectionGuideCategories) {
    const entry = category.entries.find((item) => item.id === id);
    if (entry) return entry;
  }
  return undefined;
}
